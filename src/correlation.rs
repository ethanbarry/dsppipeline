pub struct BarkerCorrelator {
    kernel: Vec<i8>,
    perfect_correlation: f32,
}

impl BarkerCorrelator {
    pub fn new() -> Self {
        Self {
            kernel: KERNEL.to_vec(),
            perfect_correlation: KERNEL
                .iter()
                .zip(&KERNEL)
                .map(|(&l, &r)| l as f32 * r as f32)
                .sum(),
        }
    }

    pub fn process(&mut self, sample: &[i8]) -> Result<Vec<f32>, ()> {
        if sample.len() < self.kernel.len() {
            return Err(());
        }

        println!("{}", sample.len() - self.kernel.len());

        Ok(sample
            .windows(self.kernel.len())
            .map(|window| {
                let score: i32 = window
                    .iter()
                    .zip(&self.kernel)
                    .map(|(&l, &r)| l as i32 * r as i32)
                    .sum();

                score.abs() as f32 / self.kernel.len() as f32
            })
            .collect())
    }
}
